// find all multiples of 3 and 5 below 1000 and add the sum

var sum = 0;
for (var i = 0; i < 10; i++) {
  if (i % 3 === 0 || i % 5 === 0) {
    sum += i;
  }
}
console.log('Sum: %d', sum);



